package hbmd.ctl.domain;

public class student {
	private int id;
	private String name;
	private String stutat;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStutat() {
		return stutat;
	}
	public void setStutat(String stutat) {
		this.stutat = stutat;
	}
	
}
